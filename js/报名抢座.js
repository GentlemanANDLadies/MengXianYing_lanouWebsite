//点击选项卡跳转
function pageControl(){
	var t=0;
	var b=Math.ceil($('.content').height()/600);
	var length=b
	var index=length
	for(var i=0;i<length;i++){
		$('<li>'+ index-- +'</li>').prependTo('.tabControl').addClass('num')
	}
	$(".num").eq(t).css("background","lightblue")
	$('.num').click(function (){
		t=$(this).index()
		$('.content').css({
			top:-t*600,
		})
		$(".num").eq(t).css("background","lightblue").siblings(".num").css("background","#fff")
		console.log(t)
	})
	$('.home_page').click(function(){
		t=0;
		$('.content').css({
			top:-t*600,
		})
		$(".num").eq(t).css("background","lightblue").siblings(".num").css("background","#fff")
	})
	$('.next_page').click(function(){
		b=parseInt($('.content').height()/600);
		t++;
		if(t>b){
			t=b;
		}
		$('.content').css({
			top:-t*600,
		})
		$(".num").eq(t).css("background","lightblue").siblings(".num").css("background","#fff")
	})
	$('.prev_page').click(function(){
		t--;
		if(t<0){
			t=0;
		}
		$('.content').css({
			top:-t*600,
		})
		$(".num").eq(t).css("background","lightblue").siblings(".num").css("background","#fff")
	})
	$('.end_page').click(function(){
		t=b;
		$('.content').css({
			top:-t*600,
		})
		$(".num").eq(t).css("background","lightblue").siblings(".num").css("background","#fff")	
	})
}
pageControl()
